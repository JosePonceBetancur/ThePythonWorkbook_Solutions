from math import log

a = int(input('Insert a number, it will be "a": '))
b = int(input('Insert a number, it will be "b": '))

print(f'a+b is {a+b}')
print(f'a-b is {a-b}')
print(f'a*b is {a*b}')
print(f'a/b is {a/b}')
print(f'The remainder of a/b is {a%b}')
print(f'The logarithm (base 10) of a is {log(a, 10)}')


